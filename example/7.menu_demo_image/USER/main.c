/**
 * @brief 全局变量定义
 * @note  u8g2: U8g2图形库核心实例，管理OLED绘图上下文
 *        u8g2_menu: U8g2菜单库实例，管理菜单状态/位图绘制/按键交互
 *        bmp/bmp2: XBM格式位图数据数组（声明在前，定义在后）
 */
u8g2_t u8g2;                  // U8g2图形库实例
u8g2_menu_t u8g2_menu;        // U8g2菜单库实例（支持位图绘制/滚动）
const uint8_t bmp[];          // 超长XBM位图数据（测试横向滚动）
const uint8_t bmp2[];         // 常规尺寸XBM位图数据（测试基础显示）

/**
 * @brief  菜单页面1 - XBM位图绘制核心演示
 * @note   核心规则：
 *         1. u8g2_MenuDrawItemXBM(x, y, 位图数组)：在菜单指定坐标绘制XBM位图
 *         2. 当x坐标超出OLED屏幕宽度（128）时，位图自动横向滚动（仅支持横向）
 *         3. 位图项可像字符串项一样绑定附加值（如按钮回调），本示例简化展示
 *         4. 目前不支持纵向超出屏幕高度（64）的位图滚动
 * @param  无
 * @retval 无
 */
void menuItem_1()
{
    // 绘制常规尺寸位图：坐标(32,32)，显示bmp2位图（无滚动）
    // 位图项支持绑定附加值（如按钮回调），用法与字符串项一致，本示例省略
    u8g2_MenuDrawItemXBM(32, 32, bmp2); 
    
    // 绘制超长位图：坐标(523,60)，显示bmp位图（x=523>128，触发横向滚动）
    u8g2_MenuDrawItemXBM(523, 60, bmp); 
}

/**
 * @brief  OLED显示封装函数
 * @note   统一绘制菜单及位图，超长位图的滚动逻辑由菜单库自动处理
 * @param  u8g2: U8g2图形库实例指针（传入全局u8g2）
 * @retval 无
 */
void oled_display(u8g2_t * u8g2)
{
    // 绘制菜单：起始坐标(0,0)，尺寸128x64（匹配OLED屏幕分辨率）
    // 菜单库自动处理位图的显示和超长内容的滚动逻辑
    u8g2_DrawMenu(&u8g2_menu, 0, 0, 128, 64);
}

/**
 * @brief  按键扫描函数（带消抖）
 * @note   处理菜单导航按键，控制超长位图的横向滚动
 * @param  time: 扫描间隔（单位：ms，与定时器中断周期一致）
 * @retval 无
 */
void keyScann(uint16_t time)
{
    // 上键扫描：KEY_1低电平有效
    u8g2_MenuKeyScannDebounce(&u8g2_menu, MENU_Key_Up, !KEY_1, time);
    // 下键扫描：KEY_2低电平有效
    u8g2_MenuKeyScannDebounce(&u8g2_menu, MENU_Key_Down, !KEY_2, time);
    // 确认键扫描：KEY_3低电平有效（本示例无确认逻辑，仅保留接口）
    u8g2_MenuKeyScannDebounce(&u8g2_menu, MENU_Key_Enter, !KEY_3, time);
}

/**
 * @brief  定时器2中断服务函数（1ms中断）
 * @note   核心功能：调用按键扫描函数，保障按键消抖和位图滚动响应
 * @param  无
 * @retval 无
 */
void tim2_IRQ(void)
{
    // 调用按键扫描函数，传入1ms扫描间隔
    keyScann(1);
}

/**
 * @brief  主函数（程序入口）
 * @note   初始化流程：底层硬件 → OLED → 菜单 → 定时器 → 主循环
 *         核心功能：显示常规/超长XBM位图，支持按键控制横向滚动
 * @param  无
 * @retval 无
 */
int main(void)
{
    // 1. 底层硬件初始化
    delay_init();       // 初始化延时函数（保障IIC/OLED通信时序）
    gpio_init();        // 初始化GPIO（按键/外设引脚配置）
	
    // 2. OLED及菜单初始化
    oled_u8g2_init(&u8g2);                    // 初始化OLED硬件
    u8g2_SetFont(&u8g2, u8g2_font_10x20_tf);  // 设置菜单默认字体（位图显示不依赖）
    u8g2_CreateMenu(&u8g2, &u8g2_menu, menuItem_1); // 创建菜单，默认显示menuItem_1
    
    // 3. 定时器初始化（1ms中断）
    tim2_init(1000-1, 72-1);                  // 72MHz时钟 → 1ms中断（预分频72-1，自动重装1000-1）
    
    // 主循环（持续刷新屏幕，响应按键控制位图滚动）
    while(1)
    {
        u8g2_ClearBuffer(&u8g2);  // 清空显存缓冲区（避免帧残留）
        oled_display(&u8g2);      // 绘制菜单及位图（自动处理滚动）
        u8g2_SendBuffer(&u8g2);   // 发送显存数据到OLED屏幕（实时刷新）
    }
}


void u8g2_MenuButton(uint8_t ID, u8g2_menuKeyValue_t key)
{
	if(key == MENU_Key_Enter)
	{
		if(ID == 1) u8g2_MenuReplaceItem(&u8g2_menu,menuItem_1);
		if(ID == 0) u8g2_MenuReplaceItem(&u8g2_menu,menuItem_2);
	}
}

